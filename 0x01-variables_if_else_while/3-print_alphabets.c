#include <stdio.h>

/**
 * main - Entry point
 * Return: Always 0 (Success)
 */

int main(void)
{
char ch;
for (ch = 'a'; ch <= 'z'; ch++) /* Print lowercase letter a-z */
{
putchar('ch');
}
for (ch = 'A'; ch <= 'Z'; ch++) /* Print Upper letters A-Z */
{
putchar('ch');
}
putchar('\n');

return (0);
}
